			<h3 class="font-bold text-1xl">Technical Committee (TC)</h3>

			The Technical Committee guards the quality of the training and is responsible for creating and subscribing competition teams. There is a separate committee for these tasks, because the board changes every year but we do not want the trainings to change every year.

			<h3 class="font-bold text-1xl pt-4">Tournament Committee (ToKo)</h3>

			The Tournament Committee is responsible for organizing the yearly Panache Tournament.

			<h3 class="font-bold text-1xl pt-4">Panache Lustrum Committee (PLuK)</h3>

			The Panache Lustrum Committee is active during the lustrum years of Panache.

			<h3 class="font-bold text-1xl pt-4">Panache Activity Committee (PAK)</h3>

			The Panache Activity Committee organises activities for all Panache members on regular basis. This committee also has a budget. The aim is to organize about two or three activities per month. These include badminton-related activities such as sending promoting for joining a tournament or visiting a high-level tournament, and non-badminton-related activities such as a movie night or picknick.

			<h3 class="font-bold text-1xl pt-4">Roaming in Mythical Belgium or Elsewhere (RiMBoE)</h3>

			The RiMBoE organizes the members weekend which is open to join for all Panache members.

			<h3 class="font-bold text-1xl pt-4">Panache Intro Committee (PIK)</h3>

			The Panache Intro Committee organizes the aspects necessary to present Panache at the TU/e Bachelor intro week and the Fontys Purple week before the start of each school year. This includes a stand at the campus market and hosting a workshop.

			<h3 class="font-bold text-1xl pt-4">The Internet Committee (DIK)</h3>

			The Internet Committee is in charge of the digital organization of Panache. This includes e-mail aliases and website permissions.

			<h3 class="font-bold text-1xl pt-4">Promotion Committee (ProCo)</h3>

			The Promotion Committee takes care of all social media accounts, and creating promotion material for activities and events.

			<h3 class="font-bold text-1xl pt-4">Sponsor Committee (SpoCo)</h3>

			The Sponsor Committee maintains the contracts and relations of our sponsors, and tries to find more sponsors for our association.

			<h3 class="font-bold text-1xl pt-4">Club Championships Committee (CKK)</h3>

			The CKK is responsible for setting up and hosting the Panache Club Championships, during which all members can compete to prove that they are the best player of Panache.

			<h3 class="font-bold text-1xl pt-4">Panache Long Term Policy Committee (PLaTBeK)</h3>

			The PLaTBeK creates the long-term policy every five years.

			<h3 class="font-bold text-1xl pt-4">Trainingsweekend Organization Committee (TOK)</h3>

			The TOK organizes a yearly trainingsweekend for all Panache members. The training weekend is two days full of badminton training, fun activities, and a sleepover.

			<h3 class="font-bold text-1xl pt-4">Finances Committee (KasCo)</h3>

			The KasCo consists of previous treasurers, checking up on the current treasurer. Big expenditures must be approved by the KasCo, as well as the yearly financial report at the end of each board year. Members cannot be in the KasCo for more than 2 consecutive years.

			<h3 class="font-bold text-1xl pt-4">The Advisory Council (RVA)</h3>

			The advisory board consists of former board members and honorable members (ereleden) with experience to give advice to the current board members. The RvA is not answering simple questions but only helping with complicated matters.
